package kkr.job.file2base.domains.common.components.iterableinput;

public class IterableInputException extends Exception {

	public IterableInputException() {
		super();
	}

	public IterableInputException(String message, Throwable cause) {
		super(message, cause);
	}

	public IterableInputException(String message) {
		super(message);
	}

	public IterableInputException(Throwable cause) {
		super(cause);
	}
}
